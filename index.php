                $content = "上传位置：纬度 ".$object->Latitude.";经度 ".$object->Longitude;
                break;
      
            default:
                $content = "receive a new event: ".$object->Event;
                break;
        }

        if(is_array($content)){
            $result = $this->transmitNews($object, $content);
        }else{
            $result = $this->transmitText($object, $content);
        }
        return $result;
    }

